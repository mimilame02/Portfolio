	<script>
        // Your list of facts
        let facts = [
			"Can't live without my cat",
			"Likes to sweet talk when seen a cat",
			"An unusual cat person..."
		];


        // Counter to keep track of current fact
        let factIndex = 0;

        // The HTML element where we will display the facts
        let factContainer = document.getElementById("factContainer");

        function typeFact(fact, charIndex) {
            if (charIndex < fact.length) {
                // Add the next character to the displayed fact
                factContainer.innerHTML += fact[charIndex];
                // Wait before typing the next character
                setTimeout(typeFact, 100, fact, charIndex + 1);
            } else {
                // Wait before starting the next fact
                setTimeout(nextFact, 1000);
            }
        }

        function nextFact() {
            // Clear the displayed fact
            factContainer.innerHTML = "";
            // If we have more facts, type the next one
            if (factIndex < facts.length) {
                typeFact(facts[factIndex], 0);
                factIndex++;
            } else {
                // If we've gone through all facts, start over
                factIndex = 0;
                nextFact();
            }
        }

        // Start typing the first fact
        nextFact();
    </script>


	<script>
		$(document).ready(function() {
			$("#contact__submit").click(function() {
				var sender_name = $("#name").val();
				var sender_email = $("#email").val();
				var message_content = $("#content").val();

				// Hide paper plane
				$(".contact__paper-plane-wrapper").hide();

				$.ajax({
					url: 'php/contact_form.php',
					type: 'POST',
					data: {
						sender_name: sender_name,
						sender_email: sender_email,
						message_content: message_content
					},
					success: function(data) {
						var response = JSON.parse(data);

						if(response.type == 'error') {
							// Handle error response
							$('.contact__response--error .contact__response-description--error').html(response.text);
							$('.contact__response--error').show();
							$('.contact__response--success').hide();
						} else {
							// Handle success response
							$('.contact__response--success .contact__response-description--success').html(response.text);
							$('.contact__response--success').show();
							$('.contact__response--error').hide();
						}
						// Show paper plane after 5 seconds
						setTimeout(function() {
							$(".contact__paper-plane-wrapper").show();
							$('.contact__response--success').hide();
						}, 5000);
					},
					error: function() {
						// Handle general ajax error
						$('.contact__response--error .contact__response-description--error').html('Error in sending message');
						$('.contact__response--error').show();
						$('.contact__response--success').hide();
						
						// Show paper plane after 5 seconds
						setTimeout(function() {
							$(".contact__paper-plane-wrapper").show();
							$('.contact__response--error').hide();
						}, 5000);
					}
				});
			});
		});
	</script>
		
