		window.onload = function() {
		var flexContainer = document.querySelector('li.d-flex');
		var flexItems = flexContainer.querySelectorAll('a');
		var perRow = 2;  // Set to desired number of items per row

		for (var i = 0; i < flexItems.length; i++) {
			if (i % perRow === 0 && i !== 0) {
			var breakElement = document.createElement('div');
			breakElement.classList.add('break');
			flexContainer.insertBefore(breakElement, flexItems[i]);
			}
		}
		};

		window.onload = function() {
		// Get the container and the links
		var modal = document.getElementById('imageContainer');
		var img = document.getElementById('modalImage');
		var links = document.querySelectorAll('.image-link');
		var span = document.getElementsByClassName("close")[0];

		// Add a click event listener to each link
		links.forEach(function(link) {
			link.addEventListener('click', function(event) {
			// Prevent the link from opening in a new tab
			event.preventDefault();

			// Set the source of the image and show the modal
			img.src = this.href;
			modal.style.display = "block";
			});
		});

		// When the user clicks on <span> (x), close the modal
		span.onclick = function() { 
			modal.style.display = "none";
		}

		// When the user clicks anywhere outside of the modal, close it
		window.onclick = function(event) {
			if (event.target == modal) {
			modal.style.display = "none";
			}
		}
		};

